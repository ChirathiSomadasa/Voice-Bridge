        }
    }

    // ── Scaler ─────────────────────────────────────────────────────────────────

    /**
     * Applies StandardScaler normalization: (x - mean) / scale
     * Must be called on ALL feature vectors before feeding to the model.
     * Scaler params come from output_mapping.json (generated at training time).
     */
    private fun scaleFeatures(raw: FloatArray): FloatArray {
        if (!scalerLoaded) {
            // No scaler — return raw values with a warning (outputs will be wrong)
            return raw.copyOf(NUM_FEATURES)
        }
        return FloatArray(NUM_FEATURES) { i ->
            val x     = if (i < raw.size) raw[i] else 0f
            val mean  = scalerMean[i]
            val scale = scalerScale[i]
            if (scale == 0f) 0f else (x - mean) / scale
        }
    }

    // ── Public API ─────────────────────────────────────────────────────────────

    /** ALWAYS returns a valid Prediction — never throws, never returns null. */
    fun predict(childId: Int, currentFeatures: FloatArray): Prediction {
        updateHistory(currentFeatures)   // stores SCALED features

        if (!modelLoaded || interpreter == null) {
            Log.w(TAG, "⚠️ Model not loaded — returning defaults")
            return Prediction.defaults()
        }

        return try {
            runInference(childId, currentFeatures)
        } catch (e: Exception) {
            Log.e(TAG, "❌ Inference failed: ${e.message}")
            Log.e(TAG, "   Stack: ${e.stackTraceToString().lines().take(5).joinToString("\n   ")}")
            Log.e(TAG, "   Returning safe defaults — game continues")
            Prediction.defaults()
        }
    }

    /**
     * Convenience call with named parameters — pads missing features with safe defaults.
     * Never throws — returns Prediction.defaults() if model is unavailable.
     */
    fun predictSafe(
        childId:           Int,
        age:               Float = 8f,
        accuracy:          Float = 0.6f,
        engagement:        Float = 0.6f,
        frustration:       Float = 0.3f,
        jitter:            Float = 0.15f,
        rt:                Float = 3000f,
        consecutiveCorrect: Float = 0f,
        consecutiveWrong:  Float = 0f
    ): Prediction {
        val raw = FloatArray(NUM_FEATURES)
        raw[0]  = age
        raw[1]  = jitter
        raw[2]  = rt * 0.6f         // itl proxy
        raw[3]  = rt
        raw[4]  = 1400f             // hesitation default
        raw[5]  = accuracy
        raw[6]  = engagement
        raw[7]  = frustration
        // raw[8..11] = error types (default 0)
        raw[12] = consecutiveCorrect
        raw[13] = consecutiveWrong
        // raw[14..15] = response_decay, tap_variance (default 0)
        raw[16] = computeAlpha(accuracy, age.toInt(), rt)
        return predict(childId, raw)
    }

    // ── Inference ──────────────────────────────────────────────────────────────

    private fun runInference(childId: Int, currentFeatures: FloatArray): Prediction {
        val interp = interpreter!!

        Log.d(TAG, "── Running inference (childId=$childId) ──")

        // Scale raw features before feeding to model
        val scaledFeatures = scaleFeatures(currentFeatures)

        Log.d(TAG, "   Raw features (first 8): ${currentFeatures.take(8).map { "%.3f".format(it) }}")
        Log.d(TAG, "   Scaled features (first 8): ${scaledFeatures.take(8).map { "%.3f".format(it) }}")
        if (!scalerLoaded) {
            Log.e(TAG, "   ❌ WARNING: scaler not loaded — features are raw, predictions are garbage!")
        }
        _lastRawFeatures = currentFeatures.copyOf()

        // Inputs — history buffer already contains scaled values (see updateHistory)
        val childIdBuf: Array<FloatArray>        = Array(1) { floatArrayOf(childId.toFloat()) }
        val historyBuf: Array<Array<FloatArray>> = buildHistoryBuffer()
        val currentBuf: Array<FloatArray>        = Array(1) { scaledFeatures }
        val inputs: Array<Any>                   = arrayOf(childIdBuf, historyBuf, currentBuf)

        Log.d(TAG, "   childId=${childIdBuf[0][0]}  historySteps=${historyBuffer.size}  scalerLoaded=$scalerLoaded")

        // Outputs — allocated using actual shapes from model (not hardcoded)
        val outputCount = interp.outputTensorCount
        val outputMap   = HashMap<Int, Any>(outputCount)
        for (i in 0 until outputCount) {
            val shape = interp.getOutputTensor(i).shape()  // actual model, not JSON
            outputMap[i] = allocateOutputBuffer(shape)
        }

        interp.runForMultipleInputsOutputs(inputs, outputMap)
        Log.d(TAG, "   ✅ Inference call completed")

        logRawOutputs(outputMap)

        val prediction = parseOutputs(outputMap)
        logParsedPrediction(prediction)
        return prediction
    }

    fun lastFeatureVector(): FloatArray = _lastRawFeatures.copyOf()
    private fun logRawOutputs(outputMap: HashMap<Int, Any>) {
        Log.d(TAG, "  Raw output values (by logical name):")
        // Log in LABEL_COLS order for easier reading
        val logOrder = listOf(
            "next_accuracy", "future_engagement", "frustration_risk", "rhythm_complexity",
            "emotion_level", "optimal_difficulty", "interaction_mode",
            "minigame_trigger", "minigame_type", "minigame_difficulty",
            "whats_missing_trigger", "connect_dots_trigger", "hidden_step_index",
            "distractor", "friend_action", "sub_routine", "motivation",
            "optimal_hint", "latency_buffer", "time_pressure"
        )
        for (name in logOrder) {
            val tfliteIdx = outputNameToIndex[name] ?: continue
            val vec = extractVector(outputMap, tfliteIdx)
            val formatted = vec.joinToString(", ") { "%.3f".format(it) }
            Log.d(TAG, "   [TFLite $tfliteIdx] $name = [$formatted]")
        }
    }

