    private fun allocateOutputBuffer(shape: IntArray): Any = when {
        shape.size == 1 -> FloatArray(shape[0])
        shape.size == 2 -> Array(shape[0]) { FloatArray(shape[1]) }
        shape.size == 3 -> Array(shape[0]) { Array(shape[1]) { FloatArray(shape[2]) } }
        else -> FloatArray(1)
    }

    private fun extractVector(outputMap: HashMap<Int, Any>, tfliteIdx: Int): FloatArray {
        val buf = outputMap[tfliteIdx] ?: return FloatArray(1)
        return when (buf) {
            is Array<*>   -> (buf[0] as? FloatArray) ?: FloatArray(1)
            is FloatArray -> buf
            else          -> FloatArray(1)
        }
    }

    private fun idx(name: String): Int =
        outputNameToIndex[name] ?: FALLBACK_ALPHA_ORDER.indexOf(name)

    private fun argmax(arr: FloatArray): Int =
        arr.indices.maxByOrNull { arr[it] } ?: 0

    private fun scalar(outputMap: HashMap<Int, Any>, name: String): Float =
        extractVector(outputMap, idx(name)).firstOrNull() ?: 0f

    private fun cls(outputMap: HashMap<Int, Any>, name: String): Int =
        argmax(extractVector(outputMap, idx(name)))

    private fun parseOutputs(outputMap: HashMap<Int, Any>): Prediction = Prediction(
        nextAccuracy        = scalar(outputMap, "next_accuracy").coerceIn(0f, 1f),
        futureEngagement    = scalar(outputMap, "future_engagement").coerceIn(0f, 1f),
        frustrationRisk     = scalar(outputMap, "frustration_risk").coerceIn(0f, 1f),
        latencyBuffer       = scalar(outputMap, "latency_buffer").coerceIn(0f, 1f),
        rhythmComplexity    = scalar(outputMap, "rhythm_complexity").coerceIn(0f, 1f),
        optimalDifficulty   = cls(outputMap, "optimal_difficulty"),
        optimalHint         = cls(outputMap, "optimal_hint"),
        friendAction        = cls(outputMap, "friend_action"),
        emotionLevel        = cls(outputMap, "emotion_level"),
        subRoutine          = cls(outputMap, "sub_routine"),
        motivation          = cls(outputMap, "motivation"),
        distractor          = cls(outputMap, "distractor"),
        minigameTrigger     = cls(outputMap, "minigame_trigger") == 1,
        minigameType        = cls(outputMap, "minigame_type"),
        minigameDifficulty  = cls(outputMap, "minigame_difficulty"),
        whatsMissingTrigger = cls(outputMap, "whats_missing_trigger") == 1,
        connectDotsTrigger  = cls(outputMap, "connect_dots_trigger") == 1,
        hiddenStepIndex     = cls(outputMap, "hidden_step_index"),
        interactionMode     = cls(outputMap, "interaction_mode"),
        timePressure        = cls(outputMap, "time_pressure"),
        fromModel           = true
    )

    // ── History ────────────────────────────────────────────────────────────────

    private fun buildHistoryBuffer(): Array<Array<FloatArray>> {
        val hist  = Array(HISTORY_STEPS) { FloatArray(NUM_FEATURES) }
        val start = maxOf(0, historyBuffer.size - HISTORY_STEPS)
        for (i in 0 until HISTORY_STEPS) {
            val srcIdx = start + i
            if (srcIdx < historyBuffer.size) hist[i] = historyBuffer[srcIdx].copyOf()
        }
        return arrayOf(hist)  // [1, 5, 17] — already scaled
    }

    /**
     * Stores SCALED features in history buffer.
     * History and current features must both be normalized before the model sees them.
     */
    private fun updateHistory(rawFeatures: FloatArray) {
        if (historyBuffer.size >= HISTORY_STEPS) historyBuffer.removeFirst()
        // Store scaled version — model was trained on normalized inputs
        historyBuffer.addLast(scaleFeatures(rawFeatures.copyOf(NUM_FEATURES)))
    }

    fun resetHistory() {
        historyBuffer.clear()
        Log.d(TAG, "History buffer reset")
    }

    // ── Utilities ──────────────────────────────────────────────────────────────

    private fun computeAlpha(accuracy: Float, age: Int, rt: Float): Float {
        val af    = 1.0f + ((age - 6) * 0.1f)
        val logRt = ln(max(1.0, rt / 1000.0) + 1.0).toFloat()
        return (accuracy * 10f * af / logRt).coerceIn(0f, 12f)
    }

    fun calculateAlpha(accuracy: Float, responseTime: Float, age: Int, errorCount: Int): Float {
        val af    = 1.0f + ((age - 6) * 0.1f)
        val rtSec = responseTime / 1000f
        return (accuracy * 10f * af /
                max(1f, ln((rtSec + errorCount + 1).toDouble()).toFloat()))
            .coerceIn(0f, 12f)
    }

    fun createInitialMetrics(age: Int): FloatArray {
        val f = FloatArray(NUM_FEATURES)
        f[0]  = age.toFloat()
        f[1]  = 0.10f   // jitter
        f[2]  = 1500f   // itl
        f[3]  = 3000f   // rt
        f[4]  = 1200f   // hesitation
        f[5]  = 0.5f    // accuracy
        f[6]  = 0.7f    // engagement
        f[7]  = 0.2f    // frustration
        f[15] = 0.08f   // tap_variance
        f[16] = 5.0f    // alpha
        return f
    }

    private fun loadModelFile(): MappedByteBuffer {
        for (path in listOf(MODEL_FILE, "ml/$MODEL_FILE")) {
            try {
                val fd = context.assets.openFd(path)
                Log.d(TAG, "✅ Found model at: $path  (${fd.declaredLength / 1024} KB)")
                return FileInputStream(fd.fileDescriptor).channel
                    .map(FileChannel.MapMode.READ_ONLY, fd.startOffset, fd.declaredLength)
            } catch (_: Exception) {}
        }
        throw Exception("Model file not found in assets ($MODEL_FILE)")
    }

    fun close() {
        interpreter?.close()
        interpreter = null
        modelLoaded = false
        Log.d(TAG, "Model closed")
    }
}


