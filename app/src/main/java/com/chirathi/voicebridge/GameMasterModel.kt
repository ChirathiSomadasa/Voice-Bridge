    // ── State ──────────────────────────────────────────────────────────────────

    private var interpreter: Interpreter? = null
    var modelLoaded = false
        private set

    // name → TFLite output tensor index (loaded from JSON or discovered at runtime)
    private val outputNameToIndex = mutableMapOf<String, Int>()
    // TFLite index → shape
    private val outputShapes = mutableMapOf<Int, IntArray>()

    // Scaler parameters loaded from output_mapping.json
    // Applied to every feature vector before feeding to the model
    private var scalerMean  = FloatArray(NUM_FEATURES) { 0f }
    private var scalerScale = FloatArray(NUM_FEATURES) { 1f }
    private var scalerLoaded = false

    private val historyBuffer = ArrayDeque<FloatArray>(HISTORY_STEPS)

    // ── Init ───────────────────────────────────────────────────────────────────

    init {
        loadModel()
    }

    private fun loadModel() {
        Log.d(TAG, "════════════════════════════════════════")
        Log.d(TAG, "  GameMasterModel — Loading v9.2")
        Log.d(TAG, "════════════════════════════════════════")
        try {
            val buffer = loadModelFile()
            interpreter = Interpreter(buffer, Interpreter.Options().apply { numThreads = 2 })

            // Load JSON first — provides output mapping AND scaler params
            val jsonLoaded = loadOutputMappingJson()

            if (!jsonLoaded) {
                // JSON missing — fall back to runtime discovery
                Log.w(TAG, "⚠️  output_mapping.json not found — using runtime discovery (less reliable)")
                Log.w(TAG, "   Copy output_mapping.json to assets/ after retraining for correct mapping")
                discoverOutputLayout()
            }

            verifyOutputMapping()
            modelLoaded = true

            Log.d(TAG, "✅ Model loaded successfully")
            Log.d(TAG, "   Input tensors:  ${interpreter!!.inputTensorCount}")
            Log.d(TAG, "   Output tensors: ${interpreter!!.outputTensorCount}")
            logInputShapes()

        } catch (e: Exception) {
            modelLoaded = false
            Log.e(TAG, "❌ Model failed to load: ${e.message}")
            Log.e(TAG, "   Game will run with default values — no crash")
        }
    }

    // ── JSON Output Mapping + Scaler Load ──────────────────────────────────────

    /**
     * Loads output_mapping.json from assets.
     * Populates outputNameToIndex, outputShapes, scalerMean, scalerScale.
     *
     * The JSON is generated by export_model() in the Python script and contains:
     *   outputs.{name}.tflite_index  → which TFLite output tensor
     *   outputs.{name}.shape         → [1, size]
     *   scaler.mean                  → 17 floats, StandardScaler mean
     *   scaler.scale                 → 17 floats, StandardScaler scale
     *
     * Returns true if loaded successfully, false if file not found or parse error.
     */
    private fun loadOutputMappingJson(): Boolean {
        Log.d(TAG, "────────────────────────────────────────")
        Log.d(TAG, "  Loading output_mapping.json")
        Log.d(TAG, "────────────────────────────────────────")

        return try {
            val jsonString = context.assets.open(MAPPING_FILE)
                .bufferedReader()
                .use { it.readText() }

            val root    = JSONObject(jsonString)
            val version = root.optString("version", "unknown")
            Log.d(TAG, "   JSON version: $version")
            Log.d(TAG, "   Output order: ${root.optString("output_order", "unknown")}")

            // ── Output mapping ─────────────────────────────────────────────
            val outputs = root.getJSONObject("outputs")
            var mappedCount = 0

            for (name in outputs.keys()) {
                val entry      = outputs.getJSONObject(name)
                val tfliteIdx  = entry.getInt("tflite_index")
                val shapeArray = entry.getJSONArray("shape")
                val verified   = entry.optBoolean("verified", false)

                val shape = IntArray(shapeArray.length()) { shapeArray.getInt(it) }
                outputNameToIndex[name] = tfliteIdx
                outputShapes[tfliteIdx] = shape
                mappedCount++

                val verifyMark = if (verified) "✅" else "⚠️ "
                Log.d(TAG, "   $verifyMark '$name' → TFLite[$tfliteIdx] shape=${shape.contentToString()}")
            }

            Log.d(TAG, "   Mapped: $mappedCount / ${FALLBACK_EXPECTED_SIZES.size} outputs")

            // ── Scaler parameters ──────────────────────────────────────────
            if (root.has("scaler")) {
                val scalerJson = root.getJSONObject("scaler")
                val meanArray  = scalerJson.getJSONArray("mean")
                val scaleArray = scalerJson.getJSONArray("scale")
                val nFeatures  = scalerJson.optInt("n_features", NUM_FEATURES)

                if (meanArray.length() != NUM_FEATURES || scaleArray.length() != NUM_FEATURES) {
                    Log.e(TAG, "   ❌ Scaler size mismatch: JSON has ${meanArray.length()} features, expected $NUM_FEATURES")
                    Log.e(TAG, "      Retrain model and regenerate output_mapping.json")
                } else {
                    scalerMean  = FloatArray(NUM_FEATURES) { meanArray.getDouble(it).toFloat() }
                    scalerScale = FloatArray(NUM_FEATURES) { scaleArray.getDouble(it).toFloat() }
                    scalerLoaded = true

                    Log.d(TAG, "   ✅ Scaler loaded: $nFeatures features")
                    Log.d(TAG, "   Scaler mean  (first 5): ${scalerMean.take(5).map { "%.2f".format(it) }}")
                    Log.d(TAG, "   Scaler scale (first 5): ${scalerScale.take(5).map { "%.2f".format(it) }}")
                }
            } else {
                Log.w(TAG, "   ⚠️  No scaler in JSON — features will NOT be normalized (predictions may be wrong)")
                Log.w(TAG, "      Regenerate output_mapping.json with updated Python export script")
            }

            if (!scalerLoaded) {
                Log.e(TAG, "   ❌ Scaler NOT loaded — raw feature values will produce garbage predictions!")
                Log.e(TAG, "      Example: rt=3000 raw vs rt≈0.1 normalized. Model only understands normalized.")
            }

            Log.d(TAG, "✅ output_mapping.json loaded successfully")
            true

        } catch (e: java.io.FileNotFoundException) {
            Log.w(TAG, "   output_mapping.json not found in assets/")
            false
        } catch (e: Exception) {
            Log.e(TAG, "   Failed to parse output_mapping.json: ${e.message}")
            false
        }
    }

    fun seedHistory(tensor: Array<Array<FloatArray>>) {
        historyBuffer.clear()
        val rows = tensor.firstOrNull() ?: return
        for (row in rows) {
            if (row.any { it != 0f }) {   // skip zero-padding rows
                historyBuffer.addLast(row.copyOf())
            }
        }
        Log.d(TAG, "History seeded: ${historyBuffer.size} non-zero steps from previous session")
    }



    // ── Runtime Output Discovery (fallback if JSON missing) ────────────────────

    /**
     * Called only when output_mapping.json is not available.
     * Attempts name matching first, then alphabetical positional fallback.
     * Less reliable than JSON — copy output_mapping.json to assets to avoid this.
     */
    private fun discoverOutputLayout() {
        val interp = interpreter ?: return
        val count  = interp.outputTensorCount

        Log.d(TAG, "────────────────────────────────────────")
        Log.d(TAG, "  Runtime Output Discovery (fallback — $count tensors)")
        Log.d(TAG, "────────────────────────────────────────")

        val tfliteOutputs = mutableListOf<Triple<Int, String, IntArray>>()
        for (i in 0 until count) {
            val tensor = interp.getOutputTensor(i)
            val name   = tensor.name() ?: "null"
            val shape  = tensor.shape()
            tfliteOutputs.add(Triple(i, name, shape))
            Log.d(TAG, "   Raw TFLite[$i] name='$name' shape=${shape.contentToString()}")
        }

        // Try name matching
        var matched = 0
        for (pythonName in FALLBACK_ALPHA_ORDER) {
            val found = tfliteOutputs.firstOrNull { (_, n, _) -> n.contains(pythonName, ignoreCase = true) }
            if (found != null) {
                outputNameToIndex[pythonName] = found.first
                outputShapes[found.first]     = found.third
                matched++
                Log.d(TAG, "   ✅ Name-match: '$pythonName' → TFLite[${found.first}]")
            }
        }

        Log.d(TAG, "   Name-matched: $matched / ${FALLBACK_ALPHA_ORDER.size}")

        if (matched == 0) {
            Log.w(TAG, "   ⚠️  Zero name matches — using alphabetical positional fallback")
            Log.w(TAG, "   ⚠️  This is UNRELIABLE. Copy output_mapping.json to assets/ to fix.")
        }

        // Positional fallback
        for ((logicalIdx, pythonName) in FALLBACK_ALPHA_ORDER.withIndex()) {
            if (!outputNameToIndex.containsKey(pythonName) && logicalIdx < tfliteOutputs.size) {
                val (tfliteIdx, tfliteName, shape) = tfliteOutputs[logicalIdx]
                outputNameToIndex[pythonName] = tfliteIdx
                outputShapes[tfliteIdx]       = shape
                Log.d(TAG, "   📌 Positional[$logicalIdx]: '$tfliteName' → '$pythonName' shape=${shape.contentToString()}")
            }
        }

        // Store shapes for all indices
        for ((tfliteIdx, _, shape) in tfliteOutputs) {
            if (!outputShapes.containsKey(tfliteIdx)) outputShapes[tfliteIdx] = shape
        }
    }

    // ── Post-load shape verification ───────────────────────────────────────────

    /**
     * Verifies every resolved output has the expected last-dimension size.
     * A mismatch means the JSON is wrong (regenerate) or model architecture changed.
     */
    private fun verifyOutputMapping() {
        Log.d(TAG, "────────────────────────────────────────")
        Log.d(TAG, "  Output Shape Verification")
        Log.d(TAG, "────────────────────────────────────────")

        var passed = 0; var failed = 0

        for ((name, expectedSize) in FALLBACK_EXPECTED_SIZES) {
            val tfliteIdx  = outputNameToIndex[name]
            val shape      = if (tfliteIdx != null) outputShapes[tfliteIdx] else null
            val actualSize = shape?.last() ?: -1

            if (actualSize == expectedSize) {
                Log.d(TAG, "   ✅ $name: expected=$expectedSize actual=$actualSize [TFLite[$tfliteIdx]]")
                passed++
            } else {
                Log.e(TAG, "   ❌ MISMATCH $name: expected=$expectedSize actual=$actualSize [TFLite[$tfliteIdx]]")
                failed++
            }
        }

        Log.d(TAG, "────────────────────────────────────────")
        if (failed == 0) {
            Log.d(TAG, "  ✅ Shape verification PASSED: $passed / ${FALLBACK_EXPECTED_SIZES.size}")
            Log.d(TAG, "  ✅ All outputs mapped correctly")
        } else {
            Log.e(TAG, "  ❌ Shape verification FAILED: $failed mismatches, $passed correct")
            Log.e(TAG, "  ❌ Regenerate output_mapping.json by re-running Python export script")
        }
        Log.d(TAG, "────────────────────────────────────────")
    }

