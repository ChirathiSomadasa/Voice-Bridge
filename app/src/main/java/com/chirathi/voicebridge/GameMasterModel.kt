/**
 * GameMasterModel — v9.2
 *
 * ══ WHAT CHANGED vs PREVIOUS VERSION ══════════════════════════════════════
 *
 *  FIX 1 — Output mapping now loaded from output_mapping.json (assets/)
 *    Old code hardcoded OUTPUT_NAMES in alphabetical order and used positional
 *    fallback. The actual TFLite outputs were NOT in alphabetical order, causing
 *    17 out of 20 shape mismatches → all values read from wrong tensors.
 *
 *    New code reads output_mapping.json generated by the Python export script.
 *    The JSON contains the verified name→tflite_index mapping, confirmed by shape
 *    checks run at export time. Android never has to guess the order.
 *
 *  FIX 2 — StandardScaler normalization applied before inference
 *    The model was trained on StandardScaler-normalized features (mean/scale from
 *    training data). Feeding raw values (rt=3000, itl=1500) produced garbage.
 *    sub_routine showing [128.529] in logs confirmed unnormalized input.
 *
 *    New code reads scaler mean/scale from output_mapping.json and applies the
 *    same normalization to both current features and history buffer entries.
 *
 * ══ DEPLOYMENT ═══════════════════════════════════════════════════════════
 *    Both files must be in app/src/main/assets/:
 *      game_master.tflite
 *      output_mapping.json    ← new required file
 *
 * ══ STILL UNCHANGED ══════════════════════════════════════════════════════
 *    All inference wraps in try/catch → game never crashes.
 *    Input shapes: child_id=[1,1], history=[1,5,17], current=[1,17].
 *    Buffer allocation uses actual shapes from getOutputTensor().
 */
